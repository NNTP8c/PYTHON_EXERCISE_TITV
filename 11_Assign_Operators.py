# Lesson 11: Assign Operators In Python - Các toán tử gán trong Python

a = int(input('a: '))

a += 2
print("a += 2 = {0}".format(a))
a -= 2
print('a -= 2 = {0}'.format(a))
a *= 2
print('a *= 2 = {0}'.format(a))
a /= 2
print('a /= 2 = {0}'.format(a))
a %= 2
print('a %= 2 = {0}'.format(a))

b = int(input('b: '))
b //= 2
print('b //= 2 = {0}'.format(b))
b **= 2
print('b **= 2 = {0}'.format(b))

