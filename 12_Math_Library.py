# Lesson 12: Math Library - Thư viện toán học math

import math
a = float(input('a: '))
b = int(input('b: '))
print('sqrt ({0}): {1}'.format(a, math.sqrt(a)))
print('fabs ({0}): {1}'.format(a, math.fabs(a)))
print('floor ({0}): {1}'.format(a, math.floor(a)))
print('ceil ({0}): {1}'.format(a, math.ceil(a)))
print('pow ({0},2): {1}'.format(a, math.pow(a,2)))

print('factorial ({0}): {1}'.format(b, math.factorial(b)))
print('sin({0}): {1}'.format(a, math.sin(a)))
print('cos({0}): {1}'.format(b, math.cos(b)))